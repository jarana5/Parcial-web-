package com.example.parcial.demo.DTO;

public class EquipoDTO {
    String nombre;
    String ciudad;
    long numcopas;
    public String getNombre() {
        return nombre;
    }
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    public String getCiudad() {
        return ciudad;
    }
    public void setCiudad(String ciudad) {
        this.ciudad = ciudad;
    }
    public long getNumcopas() {
        return numcopas;
    }
    public void setNumcopas(long numcopas) {
        this.numcopas = numcopas;
    }
}
